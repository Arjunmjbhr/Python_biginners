'''
What is Casting?

- Casting is the process of converting the value of a variable from one datatype
to another

- this is done using various built-in python functions. 

- int() converts value to an integer(whole number)
- Float() converts value to floating point number(decimal)
- str() converts value to string(text).


'''
print("type casting of datatypes...")

x = '70'
y = '-20'
print(" ")
print("x=70 and y = -20")
print(" ")
print("data type of", x, "is: ",  type(x))
print("data type of", y, "is: ", type(y))
print(" ")
print("x+y", x+y)
print(" ")
print("int(x) + int(y) =", int(x) + int(y))

print(" ")
c = 7
d = 8

print(" ")
print("c=7 and d=8")
print("type of c", type(c))
print("type of d", type(d))

print(" ")
print("float(c) + float(d) =", float(c) + float(d))

e = 9
f = 8.5

print(str(e), "and", str(f), "are strings")
